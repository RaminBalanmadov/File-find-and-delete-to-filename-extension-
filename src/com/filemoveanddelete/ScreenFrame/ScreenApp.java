    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        File folder = new File(tfPath.getText());
        String str = ".";
        File[] listfiles = folder.listFiles();
        int ind = 1;

        for (int i = 0; i < listfiles.length; i++) {
            if (listfiles[i].isFile()) {
                int index = listfiles[i].getName().lastIndexOf(str);

                if (listfiles[i].getName().substring(index + 1, listfiles[i].getName().length()).trim().equalsIgnoreCase(lblSelectedFormat.getText())) {
                    if (rbAll.isEnabled() && rbAll.isSelected()) {
                        listfiles[i].delete();
                    }

                }

            }

        }
        
        
    }//GEN-LAST:event_btnDeleteActionPerformed

