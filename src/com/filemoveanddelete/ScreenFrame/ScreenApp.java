    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        File folder = new File(tfPath.getText());
        String str = ".";
        File[] listfiles = folder.listFiles();
        int ind = 1;
        tableModel = (DefaultTableModel) tbTable.getModel();
        for (int i = 0; i < listfiles.length; i++) {
            if (listfiles[i].isFile()) {
                int index = listfiles[i].getName().lastIndexOf(str);

                if (listfiles[i].getName().substring(index + 1, listfiles[i].getName().length()).trim().equalsIgnoreCase(lblSelectedFormat.getText())) {

                    for (File listfile : listfiles) {
                        tableModel.addRow(new Object[]{ind, listfiles[i].getName(), listfiles[i].getName().substring(index, listfiles[i].getName().length())});
                        ind++;
                        break;
                    }
                    tbTable.setModel(tableModel);

                }

            }

        }
    }//GEN-LAST:event_btnSearchActionPerformed

